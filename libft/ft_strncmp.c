#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	if (n == 0)
		return (0);
	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i] && i < n - 1)
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}

//compares the first n bytes of s1 and s2 and return an integer
//lesser than, equel to, or greater than zero
// <0 s1 is lesser than s2
// >0 s1 is greater than s2
// =0 s1 is equel to s2
