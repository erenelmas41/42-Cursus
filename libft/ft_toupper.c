#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	return (c);
}

//checks if the character received as an arg is an lowercase
//if it is converts it to uppercase
//otherwise returns without change
